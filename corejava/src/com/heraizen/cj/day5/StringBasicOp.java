package com.heraizen.cj.day5;

public class StringBasicOp {

	public static void main(String[] args) {
		String str = new String("Tushar Anand");
		
		System.out.println("The length of the String : " + str.length());
		System.out.println("Name in UpperCase : " + str.toUpperCase());
		System.out.println("Name in LowerCase : " + str.toLowerCase());

	}

}
